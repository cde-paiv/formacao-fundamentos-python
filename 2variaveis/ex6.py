x, y, z = "Orange", "Banana", "Cherry"

#a) Concatenar
concatenado = x + " " + y + " " + z
print("Concatenado:", concatenado)

#b) Índice da letra "a"
print("Índice de 'a' em x:", x.find("a"))
print("Índice de 'a' em y:", y.find("a"))
print("Índice de 'a' em z:", z.find("a"))

#c) Transformar em minúsculas
x = x.lower()
y = y.lower()
z = z.lower()
print("x em minúsculas:", x)
print("y em minúsculas:", y)
print("z em minúsculas:", z)